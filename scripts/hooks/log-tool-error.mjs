#!/usr/bin/env node
/**
 * PostToolUse hook — log tool errors to memory/tool-lessons/<tool>.md so the
 * agent can learn from past failures across sessions.
 *
 * Input (stdin, JSON): { session_id, tool_name, tool_input, tool_response, ... }
 *
 * We only append when the tool result looks like a failure: non-empty stderr
 * on Bash, explicit error field, or exit_code != 0. Silent / successful calls
 * are ignored — noise in the log is worse than fewer entries.
 */

import { readFileSync, existsSync, mkdirSync, appendFileSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
import { safeMain } from './_safe.mjs';

const __dirname = dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = join(__dirname, '..', '..');
const LESSONS_DIR = join(PROJECT_ROOT, 'memory', 'tool-lessons');

function readStdinSync() {
  try {
    return readFileSync(0, 'utf8');
  } catch {
    return '';
  }
}

function detectFailure(tool, response) {
  if (!response || typeof response !== 'object') return null;
  if (response.error) return String(response.error).slice(0, 800);
  if (typeof response.exit_code === 'number' && response.exit_code !== 0) {
    const stderr = (response.stderr || '').toString().slice(0, 800);
    return `exit ${response.exit_code}${stderr ? ': ' + stderr : ''}`;
  }
  if (tool === 'Bash') {
    const stderr = (response.stderr || '').toString();
    const stdout = (response.stdout || '').toString();
    if (/error|failed|traceback|exception/i.test(stderr) && stderr.length > 0) {
      return stderr.slice(0, 800);
    }
    if (/error|traceback|exception/i.test(stdout) && /\d+\s*error/i.test(stdout)) {
      return stdout.slice(0, 800);
    }
  }
  return null;
}

function summarize(input) {
  if (!input || typeof input !== 'object') return '(no input)';
  if (input.command) return input.command.slice(0, 200);
  if (input.file_path) return input.file_path;
  if (input.pattern) return `pattern: ${input.pattern}`;
  try { return JSON.stringify(input).slice(0, 200); } catch { return '(unserializable)'; }
}

function main() {
  const raw = readStdinSync();
  if (!raw.trim()) { process.exit(0); }

  let payload;
  try { payload = JSON.parse(raw); } catch { process.exit(0); }

  const tool = payload.tool_name || 'unknown';
  const input = payload.tool_input || {};
  const response = payload.tool_response || {};

  const failure = detectFailure(tool, response);
  if (!failure) { process.exit(0); }

  mkdirSync(LESSONS_DIR, { recursive: true });
  const file = join(LESSONS_DIR, `${tool}.md`);

  if (!existsSync(file)) {
    const header = `# ${tool} — tool lessons

_Auto-generated by \`scripts/hooks/log-tool-error.mjs\`. Entries are prepended as failures occur. Review and consolidate into Always / Never / Gotchas sections when this file grows past ~10 entries._

## Recent failures
`;
    appendFileSync(file, header, 'utf8');
  }

  const ts = new Date().toISOString();
  const entry = `\n### ${ts}\n- **Call:** ${summarize(input)}\n- **Failure:** ${failure.replace(/\n/g, ' ').slice(0, 500)}\n`;
  appendFileSync(file, entry, 'utf8');

  // Hooks default to suppressing stdout, but we explicitly signal success
  // so Claude Code doesn't mistakenly think the hook failed.
  process.exit(0);
}

safeMain(main, import.meta.url);
